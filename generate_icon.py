PALETTE = {0: BLACK, 1: GREEN, 2: DIM, 3: CYAN, 4: TRANS}


def make_icon(size):
    """Scale the 32×32 design to the given size using nearest-neighbor."""
    scale = size // 32
    if scale < 1:
        scale = 1

    rows = []
    for py in range(size):
        row = []
        src_y = min(py // scale, 31)
        for px in range(size):
            src_x = min(px // scale, 31)
            val = ICON_32[src_y][src_x]
            row.append(PALETTE[val])
        rows.append(row)
    return rows


def generate_all():
    out_dir = "Assets.xcassets/AppIcon.appiconset"
    os.makedirs(out_dir, exist_ok=True)

    sizes = [
        (16,  "icon_16x16.png"),
        (32,  "icon_16x16@2x.png"),
        (32,  "icon_32x32.png"),
        (64,  "icon_32x32@2x.png"),
        (128, "icon_128x128.png"),
        (256, "icon_128x128@2x.png"),
        (256, "icon_256x256.png"),
        (512, "icon_256x256@2x.png"),
        (512, "icon_512x512.png"),
        (1024,"icon_512x512@2x.png"),
    ]

    for size, name in sizes:
        pixels = make_icon(size)
        path = os.path.join(out_dir, name)
        write_png(path, pixels, size, size)
        print(f"  ✓  {name}  ({size}×{size})")

    print("\nAll icons generated successfully.")


if __name__ == "__main__":
    generate_all()
